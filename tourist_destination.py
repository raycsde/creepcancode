tourist_destination = ['hongkong', 'macau', 'singapore', 'usa', 'australia', 'taiwan', 'new zwaland']
print(tourist_destination)
print(sorted(tourist_destination))
print(tourist_destination)
tourist_destination.reverse()#注意：reverse()是把列表的原来顺序颠倒，而不是字母排序颠倒！
print(tourist_destination)
tourist_destination.reverse()
print(tourist_destination)
tourist_destination.sort()
print(tourist_destination)
tourist_destination.sort(reverse=True)#True T is upper.
print(tourist_destination)

